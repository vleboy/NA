import { getWeek } from '@/behavior/getWeek'
export default {
  beforeCreate () {
    this.$store.commit({
      type: 'recordNowindex',
      data: 'agLiveGameReport'
    })
    this.$store.commit('returnLocalStorage')
  },
  created () {
    this.refreshList()
  },
  data () {
    return {
      isSelect_time: false, // 是否自定义搜索时间
      searchDate: getWeek(), // 搜索时间戳

      nowList: '', // 登录角色报表信息
      nowChild: [], // 登陆角色直属下级

      clickChild: [], // 点击渲染的下级
      rendered: [], // 已经渲染的下级

      nowPlayer: [], // 当前登录或点击渲染的代理玩家
      playerParent: '', // 当前玩家所属代理昵称


      parentMix: '', // 上级洗码比
      nowType: gameType('agLive'), // 当前输赢报表游戏类型
    }
  },
  activated: function () {
    this.$store.commit({
      type: 'recordNowindex',
      data: 'agLiveGameReport'
    })
  },
  computed:{
    agLiveNowlist () {
      let data = [this.nowList]
      return data
    },
    agLiveNowchild () {
      let nowChild = this.nowChild
      return nowChild
    },
    agRenderChild () {
      let child = this.clickChild
      return child
    },
    agLivePlayer () {
      let player = this.nowPlayer
      return player
    }
  },
  watch: {
    searchDate (val) {
      if (!val) {
        this.isSelect_time = false
        this.searchDate = getWeek()
      } else {
        if (typeof val[0] == 'object' || typeof val[1] == 'object') {
          if (val[0] < 1514736000000 || (val[0] < 1517796000000 && val[1] > 1517796000000)) {
            this.$message({
              type: 'error',
              message: '该时间范围暂不支持查询',
              duration: 0,
              showClose: true,
            })
            this.isSelect_time = true
          } else {
            if (val[1] < 1517796000000) {
              this.$message({
                type: 'warning',
                message: '当前时间范围查询数据为旧版报表',
                duration: 5000
              })
            }
            for (var i = val.length - 1; i >= 0; i--) {
              this.searchDate[i] = new Date(this.searchDate[i].toString()).getTime()
            }
            if (this.searchDate[1] >= new Date().getTime() - 180000) {
              if (this.searchDate[1] - this.searchDate[0] <= 180000) {
                this.searchDate[0] = this.searchDate[0] - 180000
                this.searchDate[1] = new Date().getTime() - 180000
              } else {
                this.searchDate[1] = new Date().getTime() - 180000
              }
            }
            this.isSelect_time = true
          }
        }
      }
    }
  },
  methods: {
    getTimer () {
      let timer = document.getElementsByTagName('input')
      for (let item of timer) {
        item.readOnly = true
      }
      let clear = document.getElementsByClassName('el-picker-panel__link-btn')
      for (let btn of clear) {
        btn.style.display = 'none'
      }
    }, // 原生操作时间日期控件
    refreshList (data) {
      this.$store.commit('startLoading')
      this.playerParent = ''
      this.nowPlayer = []
      this.nowChild = []
      this.clickChild = []
      this.nowList = ''
      this.getLoginSelf()
      this.getLoginChild(localStorage.loginId)
      if (localStorage.loginSuffix != 'Agent') {
        this.getLoginPlayer({
          displayName: localStorage.loginDisplayName,
          userId: localStorage.loginId
        })
      }
    }, // 初始化列表
    getLoginSelf () {
      let require = {
        userId: localStorage.loginId
      }
      let result1 = invoke({
        url: api.reportInfo,
        method: api.post,
        data: require
      }).then(result => {
        const [err, ret] = result
        if (err) {
        } else {
          var user = ret.data.payload
          user.betCount = 0
          user.betAmount = 0
          user.winAmount = 0
          user.winloseAmount = 0
          user.mixAmount = 0
          user.submit = 0
          user.winloseRate = 0
          user.nowBouns = 0
          user.nowallBet = 0
          let mix = ''
          !user.gameList ? this.parentMix = 0.01 : user.gameList.filter(mix => {return mix.code == this.nowType}).length == 0 ? this.parentMix = 0.01 : this.parentMix = user.gameList.filter(mix => {return mix.code == this.nowType})[0].mix / 100
          this.nowList = user
          if (localStorage.loginSuffix != 'Agent') {
            let time = this.isSelect_time ? this.searchDate : getWeek()
            let data = {
              gameType: gameType('agLive'),
              role: '1000',
              userIds: [localStorage.loginId],
              query: {createdAt: time}
            }
            invoke({
              url: api.calcUserStat,
              method: api.post,
              data: data
            }).then(result => {
              const [err,ret] = result
              var data = ret.data.payload
              if (data) {
                data.map(side =>{
                  if (user.userId == side.userId) {
                    user.betCount = side.betCount
                    user.betAmount = side.betAmount
                    user.winloseAmount = side.winloseAmount
                    user.mixAmount = side.mixAmount
                    user.gameList.filter(mix => {return mix.code == this.nowType}).length == 0 ? user.nowBouns = side.mixAmount * this.parentMix : user.nowBouns = side.mixAmount * user.gameList.filter(mix => {return mix.code == this.nowType})[0].mix / 100
                    user.nowallBet = user.nowBouns + side.winloseAmount
                    user.winloseRate = user.nowallBet / side.mixAmount
                    user.submit = user.nowallBet * (1 - user.rate / 100)
                  }
                })
              }
            })
          }
        }
      })
    }, // 获取登陆用户报表基本信息
    getLoginChild (id) {
      let data = {}
      localStorage.loginSuffix == 'Agent' ? data.parent = '01' : data.parent = id
      invoke({
        url: api.reportInfo,
        method: api.post,
        data: data
      }).then(
        result => {
          const [err, ret] = result
          if (err) {
          } else {
            var child = ret.data.payload
            child.length == 0 ? this.$store.commit('closeLoading') : ''
            var result = []
            var cut_count = 50 // 数组切割长度
            for (var i = 0;i < Math.ceil(child.length / cut_count);i++) {
              i == 0 ? result.push(child.slice(i, cut_count)) : result.push(child.slice(i * cut_count, cut_count * (i + 1)))
            }
            let time = this.isSelect_time ? this.searchDate : getWeek()

            let allReady = [] // promise所有结果返回
            for (let item of result) {
              let child_data = {
                gameType: gameType('agLive'),
                role: '1000',
                userIds: item.map(item=>{return item.userId}),
                query: {
                  createdAt: time
                }
              }
              let pro = new Promise((resolve, reject) => {
                invoke({
                  url: api.calcUserStat,
                  method: api.post,
                  data: child_data
                }).then(
                  result => {
                    const [err, ret] = result
                    if (err) {
                      reject(err)
                    } else {
                      var data = ret.data.payload
                      if (data.length > 0) {
                        item.map(item=> {
                          data.map(side =>{
                            if (item.userId == side.userId) {
                              item.betCount = side.betCount
                              item.betAmount = side.betAmount
                              item.winloseAmount = side.winloseAmount
                              item.mixAmount = side.mixAmount
                              item.gameList.filter(mix => {return mix.code == this.nowType}).length == 0 ? item.nowBouns = side.mixAmount * this.parentMix : item.nowBouns = side.mixAmount * item.gameList.filter(mix => {return mix.code == this.nowType})[0].mix / 100
                              item.nowallBet = item.nowBouns + side.winloseAmount
                              item.winloseRate = item.nowallBet / side.mixAmount
                              item.submit = item.nowallBet * (1 - item.rate / 100)
                            }
                          })
                        })
                        this.nowChild.push(...item.filter(item=>{
                          let isRepeat = false
                          for (let side of this.nowChild) {
                            side.userId == item.userId ? isRepeat = true : ''
                          }
                          return item.betCount > 0 && !isRepeat
                        }))
                        if(localStorage.loginSuffix == 'Agent'){
                          this.nowList.betCount = this.nowChild.map( child => child.betCount ).reduce( (a , b)=>{return a + b} , 0 )
                          this.nowList.betAmount = this.nowChild.map( child => child.betAmount ).reduce( (a , b)=>{return a + b} , 0 )
                          this.nowList.winloseAmount = this.nowChild.map( child => child.winloseAmount ).reduce( (a , b)=>{return a + b} , 0 )
                          this.nowList.mixAmount = this.nowChild.map( child => child.mixAmount ).reduce( (a , b)=>{return a + b} , 0 )
                          this.nowList.nowBouns = this.nowList.mixAmount * this.parentMix;
                          this.nowList.nowallBet = this.nowList.nowBouns + this.nowList.winloseAmount
                          this.nowList.winloseRate = this.nowList.nowallBet / this.nowList.betAmount
                          this.nowList.submit = this.nowList.nowallBet * (1 - this.nowList.rate * 0.01 )
                        }
                      }
                      resolve(data)
                    }
                  }
                )
              })
              allReady.push(pro)
            }
            let _this = this
            Promise.all(allReady).then(result => {
              _this.$store.commit('closeLoading')
            }).catch(err => {
              _this.$message({
                type: 'error',
                message: err.message
              })
              _this.$store.commit('closeLoading')
            })
          }
        }
      )
    }, // 获取登录用户直属下级
